<div class="gsv-tabset-content-col">
<h3 class="gsv-tabset-content-col-heading">Facebook is the largest platform for user data generation and intelligence. It now competes with Google for ad revenues and is starting to pull ahead in terms of user intelligence because of the magnitude and quality of its user data. It's also a go-to platform for game developers like Zynga through its API. Facebook content include news, fan pages, daily deals -all products that are becoming important for future monetization. Facebook has reached 800 million users worldwide, up from 500 million a year ago. It has enormous traffic, with its page views accounting for 25% of all traffic in the US.</h3>
