<h2 class="gsv-tabset-content-heading">About</h2>
<div class="gsv-tabset-content-col">
<h3 class="gsv-tabset-content-col-heading">Facebook is the largest social networking platform in the world with 800 million users. It experienced a user growth rate of 71% in 2010 and the site makes up 30% of global Internet users. Facebook's web traffic accounts for every 10th click on the web and the platform itself covers a variety of areas: gaming, daily deals, virtual goods, video, music, fan pages, et al. Facebook is clearly the most powerful global social networking platform today.</h3>
